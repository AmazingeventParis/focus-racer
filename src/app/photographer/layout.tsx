function PhotographerMobileNav() {
  const { data: session } = useSession();
  const [unreadCount, setUnreadCount] = useState(0);

  const fetchUnread = useCallback(async () => {
    try {
      const res = await fetch("/api/support/unread-count", { cache: "no-store" });
      if (res.ok) {
        const data = await res.json();
        setUnreadCount(data.count);
      }
    } catch {}
  }, []);

  useSSENotifications(["user_unread", "connected"], fetchUnread);
  useEffect(() => { fetchUnread(); }, [fetchUnread]);

  const moreItems = moreItemsBase.map((item) =>
    item.href === "/photographer/support" ? { ...item, badge: unreadCount } : item
  );

  return (
    <MobileNav
      mainItems={mainItems}
      moreItems={moreItems}
      roleLabel={getRoleLabel(session?.user?.role || "PHOTOGRAPHER")}
    />
  );
}

export default function PhotographerLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const { status } = useSession();
  const router = useRouter();

  useEffect(() => {
    if (status === "unauthenticated") {
      router.push("/login");
    }
  }, [status, router]);

  if (status === "loading") {
    return (
      <div className="min-h-screen flex items-center justify-center bg-slate-50">
        <div className="animate-spin w-8 h-8 border-4 border-teal-500 border-t-transparent rounded-full" />
      </div>
    );
  }

  if (status === "unauthenticated") {
    return null;
  }

  return (
    <div className="flex min-h-screen">
      <ClientSidebar />
      <PhotographerMobileNav />
      <main className="flex-1 bg-interface overflow-auto scrollbar-thin pt-14 pb-16 md:pt-0 md:pb-0">{children}</main>
    </div>
  );
}
